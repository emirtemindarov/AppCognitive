// Название: Траектория фигуры
// Краткое описание: Нажмите на фигуру в области 1, если она пересекла область 2
// Побробное описание: Фигуры будут появлятся в левой части экрана и будут двигаться вправо. Следите за их траекторией движения и попытайтесь запомнить, какие фигуры коснулись области 1. Нажмите на подходящую фигуру, когда она достигла области 2. Нажатие на неверную фигуру отнимает баллы
// Сложность: Базовая

// Предустановка задачи 2
// Название: Запоминание чисел
// Краткое описание: Вспомните сгенерированное число после выполнение ряда действий
// Побробное описание: В начале игры будет показано случайное трехзначное число. Запомните его, а затем выполните ряд простых заданий, после которых нужно будет ввести заданное число
// Сложность: Базовая

